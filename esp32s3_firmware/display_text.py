def glyph(ch):
    return FONT.get(ch.upper(), FONT.get(' ', ['...'] * 7))


def text_width(text):
    w = 0
    for ch in text:
        g = glyph(ch)
        w += len(g[0]) + 1
    return max(0, w - 1)


def draw_text_at(text, x0, y0=5, color=PINK, dim_color=DIM, clear_first=True, do_show=True):
    if clear_first:
        board.clear(False)
    x = int(x0)
    for ch in str(text):
        g = glyph(ch)
        gw = len(g[0])
        for gy, line in enumerate(g):
            y = y0 + gy
            if y < 0 or y >= ROWS:
                continue
            for gx, pix in enumerate(line):
                if pix != '#':
                    continue
                xx = x + gx
                if 0 <= xx < COLS:
                    board.set_pixel(xx, y, color)
        x += gw + 1
    if do_show:
        board.show()


def draw_centered(text, y0=5, color=PINK):
    draw_text_at(text, (COLS - text_width(text)) // 2, y0=y0, color=color)
